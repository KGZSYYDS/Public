import turtle as t
import math
t.speed(100)
t.screensize(800,600)

def baowei(x,y,height,lenth):
    t.seth(0)
    t.penup()
    t.goto(x,y)
    t.pendown()
    t.right(90)
    t.fd(height)
    t.right(90)
    t.fd(lenth)
    t.right(90)
    t.fd(height)

def shenti():
    t.penup()
    t.goto(-180, 300 + 5 * math.sin(-18))
    t.pendown()

    #身体主部分
    t.color("black","yellow")
    t.begin_fill()
    tx = -180
    ty = 300
    while tx < 180:
        t.goto(tx, ty + 5 * math.sin(tx / 10))
        tx += 1
    while ty > 19:
        t.goto(tx + 5 * math.sin(ty / 10) + 3, ty)
        ty -= 1
    t.end_fill()

    t.color("black","yellow")
    t.begin_fill()
    tx = -180
    while ty < 310:
        t.goto(tx + 5 * math.sin(ty / 10) + 3, ty)
        ty += 1
    t.penup()
    t.goto(180 + 5 * math.sin(20 / 10) + 3, 20)
    t.end_fill()

