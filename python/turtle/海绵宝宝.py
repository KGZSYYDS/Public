def meimao(x,y,degree,dx,dy):
    t.seth(0)
    t.penup()
    t.goto(x + dx, y + dy)
    t.pendown()
    t.left(degree)
    t.fd(35)
    t.right(90)
    t.fd(6)
    t.right(90)
    t.fd(35)

