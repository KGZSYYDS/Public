#部分写在函数外的语句相当于主函数，会在作为模块被调用时运行，以下语句会在主文件的第一个函数被调用之前被调用
print("该语句写在所有函数之前")
def funca():
    print("模块包文件下的funca函数调用中")

def funcb():
    print("模块包文件下的funcb函数调用中")

def funcc():
    print("模块包文件下的funcc函数调用中")

def funcd():
    print("模块包文件下的funcd函数调用中")

